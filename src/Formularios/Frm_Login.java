    private void login() {
        String user = txt_usuario.getText();
        String pass = new String(txt_clave.getPassword());

        if (trabajadorDAO.verificarCredenciales(user, pass)) {
            Frm_Principal ing = new Frm_Principal();
            ing.setVisible(true);
            ing.show();
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Datos incorrectos.");
            txt_usuario.setText("");
            txt_clave.setText("");
            txt_usuario.requestFocus();
        }
    }

